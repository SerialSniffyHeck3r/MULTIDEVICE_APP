#include "FW_BootMain.h"

#include "APP_FAULT.h"
#include "DEBUG_UART.h"
#include "u8g2_uc1608_stm32.h"

#include "FW_Input.h"
#include "FW_Mode.h"
#include "FW_SD.h"
#include "FW_UI.h"
#include "FW_Power.h"


/* -------------------------------------------------------------------------- */
/*  공개 API: resident bootloader 런타임 시작                                  */
/* -------------------------------------------------------------------------- */
void FW_BOOT_Run(void)
{
    /* ---------------------------------------------------------------------- */
    /*  유선 DEBUG UART bring-up                                               */
    /* ---------------------------------------------------------------------- */
    DEBUG_UART_Init();
    DBG_PRINTLN("[FWBOOT] DEBUG UART ready");


    /* ---------------------------------------------------------------------- */
    /* Soft Power Switch Mk2 런타임 보정                                      */
    /*                                                                        */
    /* 목적                                                                    */
    /* - bootloader ioc 가 아직 예전 POWER_HOLD(PE2) 기준이어도               */
    /*   실제 배선(PE3=OFF, PE4=PUSH)에 맞춰                                   */
    /*   OFF 라인을 LOW 로 다시 잡는다.                                        */
    /* - 이후 app jump 전까지 전원 유지 기준을 일관되게 만든다.                 */
    /* ---------------------------------------------------------------------- */
    FW_POWER_Init();

    /* ---------------------------------------------------------------------- */
    /*  부트 모드 전용 입력 / SD / UI 초기화                                    */
    /* ---------------------------------------------------------------------- */
    FW_INPUT_Init();
    FW_SD_InitRuntime();
    FW_UI_Init();

    /* ---------------------------------------------------------------------- */
    /*  reset 직후 진입 조건 평가                                               */
    /* ---------------------------------------------------------------------- */
    DBG_PRINTLN("[FWBOOT] startup condition check");
    FW_MODE_RunFromReset();

    /* ---------------------------------------------------------------------- */
    /*  FW_MODE_RunFromReset() 는 정상 경로라면 jump 하므로 return 하지 않는다. */
    /*  그래도 방어적으로 무한 루프를 둔다.                                     */
    /* ---------------------------------------------------------------------- */
    for (;;)
    {
    }
}
