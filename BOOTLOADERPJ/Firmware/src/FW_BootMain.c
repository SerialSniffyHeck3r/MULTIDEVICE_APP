#include "FW_BootMain.h"

#include "APP_FAULT.h"
#include "DEBUG_UART.h"
#include "u8g2_uc1608_stm32.h"

#include "FW_Input.h"
#include "FW_Mode.h"
#include "FW_SD.h"
#include "FW_UI.h"

/* -------------------------------------------------------------------------- */
/*  공개 API: resident bootloader 런타임 시작                                  */
/* -------------------------------------------------------------------------- */
void FW_BOOT_Run(void)
{
    /* ---------------------------------------------------------------------- */
    /*  유선 DEBUG UART bring-up                                               */
    /* ---------------------------------------------------------------------- */
    DEBUG_UART_Init();
    DBG_PRINTLN("[FWBOOT] DEBUG UART ready");

    /* ---------------------------------------------------------------------- */
    /*  부트 모드 전용 입력 / SD / UI 초기화                                    */
    /* ---------------------------------------------------------------------- */
    FW_INPUT_Init();
    FW_SD_InitRuntime();
    FW_UI_Init();

    /* ---------------------------------------------------------------------- */
    /*  reset 직후 진입 조건 평가                                               */
    /* ---------------------------------------------------------------------- */
    DBG_PRINTLN("[FWBOOT] startup condition check");
    FW_MODE_RunFromReset();

    /* ---------------------------------------------------------------------- */
    /*  FW_MODE_RunFromReset() 는 정상 경로라면 jump 하므로 return 하지 않는다. */
    /*  그래도 방어적으로 무한 루프를 둔다.                                     */
    /* ---------------------------------------------------------------------- */
    for (;;)
    {
    }
}
